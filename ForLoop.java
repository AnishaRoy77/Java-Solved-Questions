package com.Anisha;
import java.util.Scanner;

public class ForLoop {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        System.out.print("Enter number :");
        int n = in.nextInt();
        for(int num = 1 ; num <=n; num++){
            System.out.println(num + " ");

        }

    }
}
